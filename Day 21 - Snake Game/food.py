from turtle import Turtle
import random

SIZE_OF_FOOD = 0.5


class Food(Turtle):

    def __init__(self):
        super().__init__()
        self.shape("circle")
        self.penup()
        self.shapesize(stretch_len=SIZE_OF_FOOD, stretch_wid=SIZE_OF_FOOD)
        self.color("red")
        self.speed("fastest")
        self.refresh()

    def refresh(self):
        random_x = random.randint(-280, 280)
        random_y = random.randint(-280, 280)
        self.goto(random_x, random_y)
